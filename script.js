    function showQuestion() {
        const q = currentTest.questions[qIdx];
        const box = document.getElementById('quiz-box');
        answered = false;
        
        // Заголовок вопроса
        let html = `<h3 style="margin-bottom:15px;color:var(--accent)">${q.q}</h3><div id="opts"></div>`;
        box.innerHTML = html;
        
        const opts = document.getElementById('opts');
        q.options.forEach((opt, i) => {
            // Для Михельсона нет "правильного" ответа в классическом понимании, но мы используем ту же структуру
            // Если тип теста 'michelson', мы не подсвечиваем правильный/неправильный, а просто фиксируем выбор
            const btn = document.createElement('button');
            btn.className = 'quiz-opt';
            btn.textContent = opt;
            btn.onclick = () => checkAnswer(i);
            opts.appendChild(btn);
        });
    }

    function checkAnswer(idx) {
        if(answered) return;
        answered = true;
        
        // Сохраняем ответ для Михельсона
        if (currentTest.type === 'michelson') {
            michelsonAnswers.push(idx);
        } else {
            // Логика для КОС (подсветка правильного/неправильного)
            const correct = currentTest.questions[qIdx].correct; // Для КОС это null, логика внутри finishTest
            const opts = document.querySelectorAll('.quiz-opt');
            
            // Для КОС мы не знаем "правильный" индекс заранее в объекте, 
            // но мы можем подсветить выбор пользователя просто как выбранный, 
            // а результат посчитать в конце.
            opts[idx].style.background = 'rgba(100, 181, 246, 0.3)';
            opts[idx].style.borderColor = '#64b5f6';
        }

        setTimeout(() => {
            qIdx++;
            if (qIdx < currentTest.questions.length) {
                showQuestion();
            } else {
                finishTest();
            }
        }, 500); // Быстрый переход для ситуационных тестов
    }

    function finishTest() {
        const box = document.getElementById('quiz-box');
        
        if(!completed.includes(currentTest.id)) {
            completed.push(currentTest.id);
            localStorage.setItem('fox_completed', JSON.stringify(completed));
            renderTests();
        }

        if (currentTest.type === 'michelson') {
            calculateMichelsonResults(box);
        } else {
            calculateKosResults(box);
        }
    }

